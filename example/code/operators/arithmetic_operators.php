<?php

$a = 6; $b = 2;

// Identity
print("+{$b} = " . +$b . "\n");
// Negation
print("-{$a} = " . -$a . "\n");
// Addition
$c = $a + $b;
print("{$a} + {$b} = {$c}\n");
// Subtraction
$c = $a - $b;
print("{$a} - {$b} = {$c}\n");
// Multiplication
$c = $a * $b;
print("{$a} * {$b} = {$c}\n");
// Division
$c = $a / $b;
print("{$a} / {$b} = {$c}\n");
// Modulo
$c = $a % $b;
print("{$a} % {$b} = {$c}\n");
// Exponentiation
$c = $a ** $b;
print("{$a} ** {$b} = {$c}\n");
