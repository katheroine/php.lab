// Binary

print("0b1111: " . 0b1111 . PHP_EOL);
print("0B1111: " . 0B1111 . PHP_EOL);

print(PHP_EOL);

// Octal
print("017: " . 017 . PHP_EOL);
print("0o17: " . 0o17 . PHP_EOL);
print("0O17: " . 0O17 . PHP_EOL);

print(PHP_EOL);

// Decimal
print("15: " . 15 . PHP_EOL);

print(PHP_EOL);

// Hexadecimal
print("0xf: " . 0xf . PHP_EOL);
print("0Xf: " . 0Xf . PHP_EOL);
print("0xF: " . 0xF . PHP_EOL);
print("0XF: " . 0XF . PHP_EOL);

print(PHP_EOL);
